x = int(input())

# Если число 0 или отрицательное, это точно не степень двойки
if x <= 0:
    print("NO")
else:
    # Пока x четное и больше 1, делим его
    while x % 2 == 0:
        x //= 2  # Это то же самое, что x = x // 2
    
    # Если в итоге осталась единица — это была степень двойки
    if x == 1:
        print("YES")
    else:
        print("NO")